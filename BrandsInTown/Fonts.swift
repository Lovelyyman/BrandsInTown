import Foundation
import UIKit

extension UIFont {
    static func smallDateFont() -> UIFont {
        return UIFont(name: "AvenirNext-HeavyItalic", size: 12)!
    }
    
    static func mediumDateFont() -> UIFont {
        return UIFont(name: "AvenirNext-HeavyItalic", size: 15)!
    }
}
